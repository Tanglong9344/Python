# python basic operations
a = 10.0
b = 3
# arithmetic operation
print("+  : ", a + b)
print("-  : ", a - b)
print("*  : ", a * b)
print("/  : ", a / b)
print("%  : ", a % b)
print("** : ", a ** b)  # 幂运算
print("// : ", a // b)
print("// : ", -a // b) # 向下取整

# comparation operation
print("== : ", a == b)
print("<  : ", a <  b)
print("<= : ", a <= b)
print(">  : ", a >  b)
print(">= : ", a >= b)
print("!= : ", a != b)

# bit operation
a = 10
print("&  : ", a & b)
print("|  : ", a | b)
print("^  : ", a ^ b)
print("~  : ", ~a)
print(">> : ", a >> b)
print("<< : ", a << b)

# bool operation
print("and: ", a and b) # b
print("or : ", a or b)  # a
print("not: ", not a)   # False

# scientic counting(base on 10)
print (3e2)
print (3e-2)

# is and is not
print("is     : ", a is b)
print("is not : ", a is not b)