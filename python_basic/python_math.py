import math

print('min(3, 4, 5):',min(3, 4, 5))                # 最小值
print('max(3, 4, 5):',max(3, 4, 5))                # 最大值
print('PI:',math.pi)                               # 常数π
print('E:',math.e)                                 # 常数E
print('math.ceil(1.3):',math.ceil(1.3))            # 向上取整
print('math.floor(1.3):',math.floor(1.3))          # 向下取整
print('math.pow(2, 3):',math.pow(2, 3))            # 指数运算
print('math.exp(2):',math.exp(2))                  # e的指数
print('math.log(100, 10):',math.log(100, 10))      # 对数运算
print('math.log10(10):',math.log10(10))            # 以10为底的对数
print('math.log(math.pow(math.e,2)):',math.log(math.pow(math.e,2))) # 默认以e为底
print('math.sqrt(4):',math.sqrt(4))                # 平方根
print('math.fabs(-12):',math.fabs(-12))            # 绝对值
print('math.sin(math.pi / 6):',math.sin(math.pi / 6))     # 正弦函数
print('math.cos(math.pi / 3):',math.cos(math.pi / 3))     # 余弦函数
print('math.tan(math.pi / 4):',math.tan(math.pi / 4))     # 正切函数
print('math.asin(0.5):',math.asin(0.5))                   # 反正弦函数
print('math.acos(0.5):',math.acos(0.5))                   # 反余弦函数
print('math.atan(1.0):',math.atan(1.0))                   # 反正切函数
print('math.degrees(math.pi / 4):',math.degrees(math.pi / 4)) # 弧度化角度
print('math.radians(180.0):',math.radians(180.0))             # 角度化弧度

