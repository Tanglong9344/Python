##数学基础运算
import math
print (math.pi)                   #常数π
print (math.e)                    #常数E
print (math.ceil(1.3))            #向上取整
print (math.floor(1.3))           #向下取整
print (math.pow(2, 3))            #指数运算
print (math.log(10, 100))         #对数运算
print (math.log(math.e*2))        #默认以e为底
print (math.sqrt(4))              #平方根
print (math.fabs(-12))            #绝对值
print (math.sin(math.pi / 3))     #正弦函数
print (math.cos(math.pi / 3))     #余弦函数
print (math.tan(math.pi / 4))     #正切函数
print (math.asin(0.866))          #反正弦函数
print (math.acos(0.500))          #反余弦函数
print (math.atan(1.000))          #反正切函数
print (math.degrees(math.pi / 4)) #弧度化角度
print (math.radians(180.0))       #角度化弧度