# default for string
x = input("Please input: ")
print(x)

# convert to int
x = int(input("Please input: "))
print(x)

# convert to float
x = float(input("Please input: "))
print(x)

# convert to str
x = str(input("Please input: "))
print(x)

# convert to bool
x = bool(input("Please input: "))
print(x)
